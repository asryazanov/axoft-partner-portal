const ask = (question, reason) => ({ question, reason });

function salesScenario({ trigger, metric, current, data, decision, budget, next }) {
  return [
    ask(`Что стало главным поводом обсуждать задачу сейчас: ${trigger}?`, 'Выявляем событие-триггер, срочность и реальный повод для сделки.'),
    ask(`Как сегодня измеряется проблема и какие показатели нужно улучшить: ${metric}?`, 'Переводим боль в KPI, по которым можно обосновать проект.'),
    ask(`Как процесс работает сейчас и где чаще всего возникают потери: ${current}?`, 'Понимаем текущий процесс, узкие места и масштаб изменений.'),
    ask(`Какие данные и системы уже используются для контроля этой задачи: ${data}?`, 'Фиксируем ИТ-ландшафт, источники данных и будущие интеграции.'),
    ask(`Какой бизнес-эффект нужен, чтобы проект получил приоритет: ${decision}?`, 'Проверяем ценность для бизнеса и критерии принятия решения.'),
    ask(`Есть ли бюджет, программа или окно проекта на 2026-2028 годы: ${budget}?`, 'Понимаем реалистичность сделки, сроки и источник финансирования.'),
    ask('Кто принимает решение, кто влияет на выбор и кого нужно подключить к следующей встрече?', 'Строим карту ЛПР, пользователей, ИТ, финансов и функциональных владельцев.'),
    ask(`Какой следующий шаг будет удобнее согласовать: ${next}?`, 'Закрываем встречу конкретным действием с Axoft и вендорами.'),
  ];
}

